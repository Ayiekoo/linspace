import numpy as np
import matplotlib.pyplot as plt

# Create an array of 50 evenly spaced values between -5 and 5
x = np.linspace(-5, 5, 50)

# Compute the y values for each x value
y = x**2

# Plot the x and y values
plt.plot(x, y)

# Show the plot
plt.show()


